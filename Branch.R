s <- c("Spring","Summer","Fall","Winter","")

for(season in s) {
  if(season == "Spring") {
    print("So many allergies")
    
  } else if(season == "Summer") {
    print("Wonderful weather for a swim")
    
  } else if(season == "Fall") {
    print("Sweater Weather")
    
  } else if(season == "Winter") {
    print("Hot Chocolate weather")
    
  } else {
    print("I don't have a preference")
  }
}  






 
